void toggle_test(){
	uint32_t k = GPIOD->ODR;
	if (k & 0x00008000){
		k &= 0x00007fff;
	}
	else
	{
		k|= 0x00008000;
	}
	GPIOD->ODR = k;
}
void toggle_orange(){
	uint32_t k = GPIOD->ODR;
	if (k & 0x00002000){
		k &= 0x0000dfff;
	}
	else
	{
		k|= 0x00002000;
	}
	GPIOD->ODR = k;
}

void mode_common(){
	uint32_t k = GPIOD->ODR;
		if (k & 0x00008000){
			k &= 0x00007fff;
		}
		else
		{
			k|= 0x00008000;
		}
		GPIOD->ODR = k;
}
int delay(int msec){
	SysTick->CTRL = 0;
	SysTick->CTRL = 	SYSTICK_CLKSOURCE_HCLK | SysTick_CTRL_TICKINT_Msk | SysTick_CTRL_ENABLE_Msk;
	SysTick->VAL = 0;
	SysTick->LOAD = ticks;
	cnt_ms = 0;
	uint32_t ret = false;
	while(msec > cnt_ms){
		if (button_toggle_access == true && (GPIOA->IDR & 0x00000001) == 0){
			button_toggle_access = false;
			ret = true;
			break;
		}

	}
	SysTick->CTRL = 0;
	return ret;
}
